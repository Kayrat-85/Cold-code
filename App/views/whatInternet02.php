                            <p>
                                В 1995 году NSFNet вернулась к роли исследовательской сети, маршрутизацией всего трафика интернета теперь занимались сетевые провайдеры, 
                                а не суперкомпьютеры Национального научного фонда.
                            </p>
                            <p>
                                В том же 1995 году Всемирная паутина стала основным поставщиком информации в интернете, обогнав по трафику протокол пересылки файлов FTP. 
                                Был образован Консорциум Всемирной паутины (W3C). Можно сказать, что Всемирная паутина преобразила интернет и создала его современный облик. 
                                С 1996 года Всемирная паутина почти полностью подменяет собой понятие «интернет».
                            </p>
                            <p>
                                В 1990-е годы интернет объединил в себе большинство существовавших тогда сетей (хотя некоторые, как Фидонет, остались обособленными). 
                                Объединение выглядело привлекательным благодаря отсутствию единого руководства, а также благодаря открытости технических стандартов интернета, 
                                что делало сети независимыми от бизнеса и конкретных компаний. К 1997 году в интернете насчитывалось уже около 10 млн компьютеров, 
                                было зарегистрировано более 1 млн доменных имён. Интернет стал очень популярным средством для обмена информацией.
                            </p>
                            <p>
                                В настоящее время подключиться к интернету можно через спутники связи, радио-каналы, кабельное телевидение, телефон, сотовую связь, 
                                специальные оптико-волоконные линии или электропровода. Всемирная сеть стала неотъемлемой частью жизни в развитых и развивающихся странах.
                            </p>
