                            которая была составлена из более мелких сетей (включая известные тогда сети Usenet и Bitnet) и 
                            имела гораздо бо́льшую пропускную способность, чем ARPANET. К этой сети за год подключились около 10 тыс. 
                            компьютеров, название «интернет» начало плавно переходить к NSFNet.
                          </p>
                          <p>
                            В 1988 году был разработан протокол Internet Relay Chat (IRC), благодаря чему в интернете стало 
                            возможно общение в реальном времени (чат).
                          </p>
                          <p>
                            В 1989 году в Европе, в стенах Европейского совета по ядерным исследованиям (ЦЕРН) родилась 
                            концепция Всемирной паутины. Её предложил знаменитый британский учёный Тим Бернерс-Ли. 
                            Он же в течение двух лет разработал протокол HTTP, язык HTML и идентификаторы URI.
                          </p>
                          <p>
                            Соавтор Тима Бернерса-Ли по формулировке целей и задач проекта World Wide Web в ЦЕРН, бельгийский 
                            исследователь Роберт Кайо, разъяснял позднее его понимание истоков этого проекта:
                          </p>
                          
                            <blockquote>
                                <em>История всех великих изобретений, как это давно и хорошо известно, базируется на большом числе им 
                                предшествующих. В случае Всемирной паутины (WWW) следовало бы в этом контексте, видимо, отметить 
                                по крайней мере два важнейших для успеха проекта пути развития и накопления знаний и технологий: 1) 
                                история развития систем типа гипертекста …; 2) Интернет-протокол, который, собственно, и сделал 
