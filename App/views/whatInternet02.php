                                (т. н. «дозво́н», англ. dialup access).
                            </p>
                            <strong>Всемирная паутина</strong>
                            <p>
                                В 1991 году Всемирная паутина стала общедоступна в интернете, а в 1993 году появился знаменитый 
                                веб-браузер NCSA Mosaic. Всемирная паутина набирала популярность.
                            </p>
                            <p>
                                Можно считать, что существуют две ясно различимые эры в истории Web: [до браузера Mosaic] Марка Андриссена и после.
                            </p>
                            <p>
                                Именно сочетание веб-протокола от Тима Бернерс-Ли, который обеспечивал коммуникацию, и браузера (Mosaic) от Марка Андриссена, 
                                который предоставил функционально совершенный пользовательский интерфейс, создало условия для наблюдаемого взрыва (интереса к Веб). 
                                За первые 24 месяца, истекшие после появления браузера Мosaic, Web прошёл стадию от полной неизвестности (за пределами считанного 
                                числа людей внутри узкой группы учёных и специалистов лишь одного мало кому известного профиля деятельности) до полной и абсолютно 
                                везде в мире его распространённости.
