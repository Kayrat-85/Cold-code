                                появился другой фундаментальный труд А. И. Китова, написанный вместе с Н. А. Криницким — „Электронные цифровые машины 
                                и программирование“. Это была фактически энциклопедия науки об ЭВМ. Многие поколения студентов в университетах и вузах 
                                страны с помощью этой замечательной книги получили фундаментальное образование и стали первоклассными учёными во многих 
                                областях знаний. Книги А. И. Китова, написанные в начале эры ЭВМ в нашей стране, не должны быть забыты». Эта книга была 
                                издана в Румынии, Венгрии, Германской демократической республике и ряде других стран. Второе стереотипное издание книги 
                                «Электронные цифровые машины и программирование» появилось в 1961 году. Общий тираж иностранных и двух советских изданий 
                                составил свыше 130 000 экземпляров. Об этом учебнике-энциклопедии в своих воспоминаниях ветеран кафедры вычислительной 
                                техники МЭИ (первая кафедра ЭВМ в стране), д. т. н., профессор А. К. Поляков написал так: «По-моему мнению, 
                                учебник А. И. Китова и Н. А. Криницкого „Электронные цифровые машины и программирование“ (1959) в то время был лучшим в мире». 
